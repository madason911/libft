#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char *tmp;
	char sym;

	tmp = (char*)s;
	sym = '\0';
	while (n > 0)
	{
		*tmp = sym;
		tmp++;
		n--;
	}
}
